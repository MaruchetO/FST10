// Primative Data Types

// String enclosed in either '' or ""
let firstName = "John";
let lastName = "Doe";
console.log("First Name:", firstName);
console.log("Last Name:", lastName);

// typeof operator: checks the data type of a variable
console.log("Data Type of firstName:", typeof firstName);
console.log("Data Type of lastName:", typeof lastName);

// Number: numeric value
let age = 7;
let pi = 3.14;
console.log("Age:", age);
console.log("Data Type of age:", typeof age);
console.log("Data Type of pi:", typeof pi);

// Boolean: Either true or false
let isStudent = false;
console.log("is a student?", isStudent);
console.log("Data Type of isStudent:", typeof isStudent);

// Null: Intentional absence of value
let empty = null;
console.log("Empty:", empty);
console.log("Data type of empty:", typeof empty);

// Undefined: Unintentional absence of value
let city;
// let city = undifined;
console.log("City:", city);
console.log("Data type of city:", typeof city);
